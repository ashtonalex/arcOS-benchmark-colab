"""
Data models for the SARM benchmark layer.

Pure dataclasses with no external dependencies beyond the standard library.
"""

from dataclasses import dataclass, field
from typing import Optional


# ========== Failure Mode Constants ==========

SPATIAL_NEGLECT = "Spatial Neglect"
TEMPORAL_CONFUSION = "Temporal Confusion"
SPURIOUS_ATTRIBUTION = "Spurious Attribution"
CAUSAL_SHORTCUTTING = "Causal Shortcutting"
COUNTERFACTUAL_BLINDNESS = "Counterfactual Blindness"

ALL_FAILURE_MODES = [
    SPATIAL_NEGLECT,
    TEMPORAL_CONFUSION,
    SPURIOUS_ATTRIBUTION,
    CAUSAL_SHORTCUTTING,
    COUNTERFACTUAL_BLINDNESS,
]

# ========== Complexity Tier Constants ==========

TIER_CALIBRATION = "Calibration"
TIER_STANDARD = "Standard"
TIER_ADVERSARIAL = "Adversarial"


def classify_tier(entity_count: int) -> str:
    """Classify scenario tier by entity count (Section 7.1)."""
    if entity_count <= 5:
        return TIER_CALIBRATION
    elif entity_count <= 15:
        return TIER_STANDARD
    else:
        return TIER_ADVERSARIAL


# ========== Probe Models ==========


@dataclass
class Probe:
    """A counterfactual probe generated by the Inquisitor (Section 3.2.3)."""
    target_node: str
    probe_type: str  # One of ALL_FAILURE_MODES
    question: str
    rationale: str
    expected_behavior: str
    attention_score: Optional[float] = None
    role_in_subgraph: Optional[str] = None


@dataclass
class ValidatedProbe:
    """A probe that has passed the Question Bank Validator (Section 3.3)."""
    probe: Probe
    robustness_score: str  # "ROBUST" or "NOT ROBUST"
    ground_truth_verifiable: bool
    ablation_test_passed: bool


# ========== Judge Models ==========


@dataclass
class JudgeVerdict:
    """Evaluation from a single judge (Section 3.4)."""
    model_id: str
    hallucination_detected: bool
    hallucination_rule: Optional[str] = None  # e.g., "Rule 1"
    hallucination_evidence: Optional[str] = None
    causal_consistency: float = 3.0       # C: 1-5
    spatiotemporal_coherence: float = 3.0  # P: 1-5
    attribution_accuracy: float = 3.0      # A: 1-5
    uncertainty_calibration: float = 3.0   # U: 1-5
    justification_c: str = ""
    justification_p: str = ""
    justification_a: str = ""
    justification_u: str = ""


@dataclass
class AggregatedScore:
    """Aggregated score across multiple judges (Section 5)."""
    causal_consistency: float
    spatiotemporal_coherence: float
    attribution_accuracy: float
    uncertainty_calibration: float
    phi: float
    sarm_score: float
    hallucination_detected: bool
    disagreement_flags: dict = field(default_factory=dict)


# ========== Result Models ==========


@dataclass
class ProbeResult:
    """Result for a single probe within a scenario."""
    probe: Probe
    base_response: str
    probe_response: str
    verdicts: list  # list[JudgeVerdict]
    aggregated_score: AggregatedScore


@dataclass
class ScenarioResult:
    """Full result for one scenario execution."""
    scenario_id: str
    question: str
    entity_count: int
    tier: str
    probe_results: list  # list[ProbeResult]
    aggregated_score: AggregatedScore
    run_index: int = 0


@dataclass
class TierScore:
    """Aggregated scores for a complexity tier."""
    tier: str
    mean_c: float
    mean_p: float
    mean_a: float
    mean_u: float
    mean_phi: float
    mean_sarm: float
    std_sarm: float
    scenario_count: int


@dataclass
class FailureModeAnalysis:
    """Failure rate for a specific failure mode."""
    failure_mode: str
    failure_rate: float
    example_scenario_id: Optional[str] = None


@dataclass
class BenchmarkReport:
    """Complete SARM benchmark report (Section 10)."""
    scenarios: list  # list[ScenarioResult]
    tier_scores: list  # list[TierScore]
    failure_mode_analysis: list  # list[FailureModeAnalysis]
    hallucination_count: int
    total_scenarios: int
    high_variance_scenarios: list = field(default_factory=list)  # scenario IDs with σ > 0.5
